;DECLARADA EN LA .DATA LLAMADA parcial CON 15 CARACTERES.LUEGO QUE IMPRIMA LA 
;CADENA AL REVES 

TITLE EJERCICIO2_PARCIAL2
INCLUDE IRVINE32.INC 
.DATA 
	PARCIAL BYTE "AQUI HAY QUINCE XD",0
.CODE
	AIUDA PROC
		MOV ESI,OFFSET PARCIAL
		MOV EAX,0
		MOV ECX,LENGTHOF PARCIAL -1
		SALTO1:MOV AL,[ESI]
			PUSH AX
			INC ESI
		LOOP SALTO1 
		
		MOV ESI,OFFSET PARCIAL 
		MOV ECX,LENGTHOF PARCIAL -1 
		MMM:POP AX 
			MOV [ESI],AL 
			INC ESI 
		LOOP MMM 
		MOV EDX,OFFSET PARCIAL 
		CALL WRITESTRING 	
	EXIT
	AIUDA ENDP
	END AIUDA 