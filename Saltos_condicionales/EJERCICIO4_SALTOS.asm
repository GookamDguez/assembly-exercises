;4) Leer EAX desde consola e indicar si esta en uno de los siguientes rangos:
;- Esta en el rango menos de 100
;- Esta en el rango >100 y <=1000
;- Esta en el rango >1000 y <=3000
;- Es mayor a 3000
TITLE EJERCICIO4
INCLUDE IRVINE32.INC
;@AUTHOR: GABRIELA MARISOL DOMINGUEZ HERNANDEZ
;@COUNTRY: EL SALVADOR, CENTRO AMERICA
.DATA
	MINUS100 BYTE " ESTA EN EL RANGO MENOS DE 100",0
	MAYOR100YMINEQ1000 BYTE " ESTA EN EL RANGO >100 Y <=1000",0
	MAYOR1000YMINUEQ3000 BYTE " ESTA EN EL RANGO >1000 Y <=3000",0
	MAYOR3000 BYTE " ES MAYOR A 3000",0
	SMS BYTE "INGRESE UN NUMERO: ",0
.CODE
	MANGOVERDE PROC
		CALL CLRSCR
		MOV EDX, OFFSET SMS
		CALL WRITESTRING
		CALL READDEC
		CALL CRLF
		;COMPARANDO Y BUSCANDO EL RANGO / LET'S START COMPARING AND FINDING THE RANK
		CMP EAX,100
		JBE RANGO1	;<=100
		JA RANGO2 	;>100
		
		RANGO1: MOV EDX, OFFSET MINUS100
				CALL WRITESTRING
				CALL CRLF
				JMP SALIR
				
		RANGO2: CMP EAX,1000	
				JBE RANGO2_PART2	;<=1000
				JA RANGO3			;>1000
				
		RANGO2_PART2: MOV EDX, OFFSET MAYOR100YMINEQ1000
					CALL WRITESTRING
					CALL CRLF
					JMP SALIR
					
		RANGO3: CMP EAX, 3000
				JBE RANGO3_PART2	;<=3000
				JA RANGO4			;>3000
				
		RANGO3_PART2: MOV EDX, OFFSET MAYOR1000YMINUEQ3000
					CALL WRITESTRING
					CALL CRLF
					JMP SALIR
				
		RANGO4: MOV EDX, OFFSET MAYOR3000
				CALL WRITESTRING
				CALL CRLF
				JMP SALIR
		
		SALIR: EXIT
		
	MANGOVERDE ENDP
	END MANGOVERDE