; 1- SUMAR EN EAX, LOS VALORES Y1,Y2,Y3
; 2- ALMACENAR X2 EN BX Y RESTARLE LOS VALORES DE Y1 AND Y3
; 3- ASIGNAR EN CH EL VALOR DE X3, SUME EL VALOR DE Y3 E INCREMENTE EN 1 CH
; 4- GUARDAR EN DX, LA FORMULA DX=(Y1+X2-Y3)+2
; 5- MOSTRAR REGISTROS DE PROPOSITO GENERAL.

TITLE VALER_QUESO
INCLUDE IRVINE32.INC
;@AUTHOR: GABRIELA DOMINGUEZ HERNANDEZ
;@COUNTRY: EL SALVADOR, CENTRO AMERICA
.DATA
	X1 WORD 53
	X2 BYTE 71
	X3 DWORD 97
	Y1 BYTE 243
	Y2 DWORD 35
	Y3 WORD 69
.CODE
	MAIN PROC
	CALL CLRSCR
	;PUNTO 1
	MOV EAX, 0
	MOV BL, Y1
	ADD EAX, EBX	;EAX + Y1
	MOV BX, Y3
	ADD EAX, EBX	; EAX + Y3
	ADD EAX, Y2	; EAX + Y2
	
	;PUNTO 2
	; ALMACENAR X2 EN BX Y RESTARLE LOS VALORES DE Y1 AND Y3
	MOV EBX,0
	MOV ECX,0
	MOV BX, WORD PTR X2
	SUB BX, WORD PTR Y1
	SUB BX, Y3
	
	;PUNTO 3
	;ASIGNAR EN CH EL VALOR DE X3, 
	;SUME EL VALOR DE Y3 E INCREMENTE EN 1 CH
	MOV ECX,0
	MOV CH, BYTE PTR X3
	ADD CH, BYTE PTR Y3
	INC CH
	
	;PUNTO 4
	;GUARDAR EN DX, LA FORMULA DX=(Y1+X2-Y3)+2
	MOV EDX,0
	ADD DX, WORD PTR Y1
	ADD DX, WORD PTR X2
	SUB DX, Y3
	ADD DX, 2
	
	;PUNTO 5
	CALL CRLF
	CALL DUMPREGS
	
	EXIT
	
	MAIN ENDP
	END MAIN
